# backend/api/routers/agents.py

from pydantic import BaseModel
from fastapi import APIRouter, Depends, HTTPException, status
from backend.api.dependencies import get_agent_service, require_role
from backend.services.agent_service import DelinquencyAgent

router = APIRouter(prefix="/agent", tags=["agent"])

class ChatRequest(BaseModel):
    customer_id: int
    message: str

class ChatResponse(BaseModel):
    response: str

@router.post("/chat", response_model=ChatResponse)
def chat_with_agent(
    payload: ChatRequest,
    agent: DelinquencyAgent = Depends(get_agent_service),
    _ = Depends(require_role(["admin", "analyst"]))
):
    """Executes conversational queries through the LangGraph risk officer agent graph."""
    try:
        result = agent.run_query(payload.customer_id, payload.message)
        return {"response": result.get("final_response", "No response generated by risk agent.")}
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Agent workflow execution failed: {str(e)}"
        )
